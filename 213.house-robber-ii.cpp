class Solution {
public:
    int rob(vector<int>& nums) {
        auto n = nums.size();
        if (n==1) 
            return nums[0];
        struct {
            int n1,n2,n3,n4;
        } dp = {
            nums[0], INT_MIN, INT_MIN, 0
        };
        for (int i=1; i<n; i++)
            dp = {
                dp.n2+nums[i], max(dp.n1, dp.n2),
                dp.n4+nums[i], max(dp.n3, dp.n4)
            };
        return max({dp.n2, dp.n3, dp.n4});
    }
};
