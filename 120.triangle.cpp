class Solution {
public:
    int minimumTotal(vector<vector<int>> &triangle) {
        int n = triangle.size();
        for (int i = 1; i < n; i++) {
            auto &prv = triangle[i - 1],
                 &cur = triangle[i];
            cur.front() += prv.front();
            for (int j = 1; j < i; j++)
                cur[j] += min(prv[j], prv[j - 1]);
            cur.back() += prv.back();
        }
        return ranges::min(triangle.back());
    }
};
